public class Ej05 {
    // Llamamos al metodo
    public static void main(String[] args) {
        // Declaramos clave primitiva entera
        int aa;
        // Inicializamos aa
        aa = 20;
        // Declaramos variable primitiva byte
        byte bb;
        // Inicializamos
        bb = 3;
        // Declaramos variable primitiva short
        short ss;
        // Inicializamos
        ss = 4;
        // Declaramos variable primitiva long
        long ll;
        // Inicializamos
        ll = 5l;
        // Declaramos variable primitiva float
        float ff;
        // Inicializamos
        ff = 3.3f;
        // Declaramos variable primitiva char
        char cc;
        // Inicializamos
        cc = 'l';
        // Declaramos variable primitiva double
        double numero;
        // Inicializamos
        numero = 4;
        // Declaramos variable primitiva String
        String caracter;
        // Inicializamos
        caracter = "hola";
        // Declaramos variable primitiva boolean
        boolean booleana;
        // Inicializamos
        booleana = true;
        // Declaramos final int
        final int CONSTANTE_ENTERA = 3;
        // Declaramos constante double
        final double CONSTANTE_DOUBLE = 5;
        
        System.out.println("La variable int pesa 32 bits y contiene el valor: " + aa);
        System.out.println("La variable double pesa 64 bits y contiene el valor: " + numero);
        System.out.println("La variable String pesa 0 bits y contiene el valor: " + caracter);
        System.out.println("La variable boolean pesa 1 bits y contiene el valor: " + booleana);
    }
 
}
